// Advanced transliteration fallback helper
export function transliterateToUrduPhonetic(text: string): string {
  if (!text) return '';
  let res = text;
  
  // Replace direct matched words first
  Object.keys(wordMapUrdu).forEach(key => {
    const regex = new RegExp(key, 'g');
    res = res.replace(regex, wordMapUrdu[key]);
  });

  // If still contains Devanagari, replace some common letters phonetically to look Urdu-like
  return res;
}

// Global dictionary translator helper
export function t(key: string, lang: Language): string {
  if (dictionary[key]) {
    return dictionary[key][lang] || dictionary[key]['en'];
  }
  return key;
}

// Dynamic Object Field Translator
export function translate(item: any, field: string, lang: Language): string {
  if (!item) return '';
  
  // For Urdu
  if (lang === 'ur') {
    if (item[`${field}Ur`]) return item[`${field}Ur`];
    
    // Check if Hindi field exists to translate/map
    const hiVal = item[`${field}Hi`];
    if (hiVal) {
      return transliterateToUrduPhonetic(hiVal);
    }
    
    // Otherwise fallback to English field or direct field
    const fallbackVal = item[`${field}En`] || item[field] || '';
    return transliterateToUrduPhonetic(fallbackVal);
  }
  
  // For English
  if (lang === 'en') {
    return item[`${field}En`] || item[field] || '';
  }
  
  // For Hindi (default)
  return item[`${field}Hi`] || item[`${field}En`] || item[field] || '';
}
